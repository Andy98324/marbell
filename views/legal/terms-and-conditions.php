<h2>1. Servicio</h2>
<p>Transfer Erkaitand S.L.U. presta servicios de transporte privado de pasajeros (traslados) con recogida en aeropuertos, estaciones, puertos y direcciones (hoteles, viviendas, etc.).</p>

<h2>2. Proceso de reserva</h2>
<ul>
  <li>El cliente solicita una reserva mediante el formulario.</li>
  <li>La reserva se considera confirmada cuando el cliente recibe confirmación (por email, WhatsApp u otro medio indicado).</li>
  <li>El cliente debe revisar los datos de recogida (fecha/hora, lugar, número de pasajeros, vuelo/tren/barco si aplica).</li>
</ul>

<h2>3. Precios y pagos</h2>
<p>El precio se muestra/indica durante el proceso de reserva o en la confirmación. Los pagos se realizarán mediante los métodos disponibles en el sitio.</p>

<h2>4. Cancelaciones</h2>
<ul>
  <li><strong>Cancelación gratuita:</strong> hasta 24 horas antes de la hora programada del servicio.</li>
  <li><strong>Cancelación fuera de plazo:</strong> puede conllevar cargos totales o parciales, en función del caso y de los costes ya incurridos.</li>
</ul>

<h2>5. Tiempos de espera</h2>
<ul>
  <li><strong>Aeropuertos (llegadas):</strong> el conductor esperará hasta <strong>1 hora</strong> desde la hora de llegada del vuelo.</li>
  <li><strong>Estaciones de tren / bus / puertos marítimos / recogidas en direcciones:</strong> el conductor esperará hasta <strong>15 minutos</strong> desde la hora programada.</li>
</ul>
<p>Si el cliente prevé retraso, debe avisar lo antes posible mediante los canales de contacto facilitados.</p>

<h2>6. No-show (no presentación)</h2>
<p>Se considera <strong>no-show</strong> cuando el cliente no se presenta dentro del tiempo de espera indicado y no hay contacto efectivo. En caso de no-show, el servicio podrá considerarse prestado y no reembolsable, salvo que la normativa aplicable disponga lo contrario o exista causa justificada acreditada.</p>

<h2>7. Derecho de desistimiento</h2>
<p>Al tratarse de un servicio de transporte de pasajeros, el régimen general de desistimiento de los contratos a distancia del TRLGDCU no resulta aplicable en los términos previstos para este tipo de contratos, sin perjuicio de los deberes de información que correspondan. Por ello, se aplica la política de cancelación indicada.</p>

<h2>8. Obligaciones del cliente</h2>
<ul>
  <li>Facilitar datos veraces y completos (especialmente lugar, fecha/hora y, si aplica, número de vuelo/tren/barco).</li>
  <li>Estar localizable por teléfono/WhatsApp en el momento de la recogida cuando sea necesario para coordinarla.</li>
</ul>

